export interface TenantProfile {
  name:             string
  logo_url?:        string | null
  company_address?: string | null
  company_phone?:   string | null
  company_email?:   string | null
  company_website?: string | null
  contact_person?:  string | null
}

// ── Colors ────────────────────────────────────────────────────────────────────
const C = {
  navy:        rgb(0.086, 0.204, 0.510),
  white:       rgb(1,     1,     1),
  black:       rgb(0.067, 0.067, 0.067),
  muted:       rgb(0.45,  0.45,  0.45),
  faint:       rgb(0.80,  0.80,  0.80),
  tableHeader: rgb(0.945, 0.949, 0.961),
  rowAlt:      rgb(0.975, 0.978, 0.984),
  termsBox:    rgb(0.970, 0.970, 0.970),
  accent:      rgb(0.145, 0.337, 0.922),
  positive:    rgb(0.055, 0.604, 0.408),
  negative:    rgb(0.847, 0.208, 0.208),
  ruleLine:    rgb(0.88,  0.88,  0.88),
  rowRule:     rgb(0.920, 0.920, 0.920),
}

function wrapText(rawText: string, font: PDFFont, size: number, maxWidth: number): string[] {
  const result: string[] = []
  for (const paragraph of rawText.split(/\r?\n/)) {
    const words = paragraph.split(' ')
    let line = ''
    for (const word of words) {
      const test = line ? `${line} ${word}` : word
      if (font.widthOfTextAtSize(test, size) > maxWidth) {
        if (line) result.push(line)
        line = word
      } else {
        line = test
      }
    }
    result.push(line)   // always push, even empty (preserves blank lines between paragraphs)
  }
  return result.length ? result : ['']
}

